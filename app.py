# ==================== 营养计算引擎 ====================

def calculate_bmi(weight_kg: float, height_cm: float) -> float:
    """计算BMI"""
    height_m = height_cm / 100
    return round(weight_kg / (height_m ** 2), 1)


def calculate_standard_weight(height_cm: float, gender: str) -> float:
    """计算标准体重（Broca改良公式）"""
    if gender == "male":
        return round((height_cm - 100) * 0.9, 1)
    else:
        return round((height_cm - 100) * 0.9 - 2.5, 1)


def calculate_bmr(weight_kg: float, height_cm: float, gender: str, age: int = 35) -> float:
    """Mifflin-St Jeor 公式计算基础代谢率"""
    if gender == "male":
        bmr = 10 * weight_kg + 6.25 * height_cm - 5 * age + 5
    else:
        bmr = 10 * weight_kg + 6.25 * height_cm - 5 * age - 161
    return round(bmr)


def calculate_daily_calories(bmr: float, activity_level: str = "light") -> float:
    """根据活动量计算每日所需热量"""
    factor = {"sedentary": 1.2, "light": 1.375, "moderate": 1.55, "active": 1.725, "very_active": 1.9}
    return round(bmr * factor.get(activity_level, 1.375))


def calculate_macros(calories: float) -> dict:
    """根据热量分配三大营养素（蛋白15%，脂肪25%，碳水60%）"""
    protein_cal = calories * 0.15
    fat_cal = calories * 0.25
    carb_cal = calories * 0.60

    return {
        "calories": round(calories),
        "protein_g": round(protein_cal / 4, 1),
        "fat_g": round(fat_cal / 9, 1),
        "carb_g": round(carb_cal / 4, 1),
    }


def calc_family_nutrition(members: list[FamilyMember]) -> dict:
    """计算全家营养需求"""
    total_calories = 0
    individual = []

    for m in members:
        bmi = calculate_bmi(m.weight, m.height)
        std_weight = calculate_standard_weight(m.height, m.gender)

        # 超重者用标准体重计算
        calc_weight = std_weight if bmi >= 24 else m.weight

        bmr = calculate_bmr(calc_weight, m.height, m.gender)
        daily_cal = calculate_daily_calories(bmr)

        # 儿童特殊调整
        if m.height < 150 and "女儿" in m.relationship or "儿子" in m.relationship:
            daily_cal = round(daily_cal * 0.85)  # 儿童适当减少

        macros = calculate_macros(daily_cal)
        total_calories += daily_cal

        individual.append({
            "id": m.id,
            "name": m.name,
            "bmi": bmi,
            "standard_weight": std_weight,
            "daily_calories": daily_cal,
            "macros": macros,
            "note": "使用标准体重计算" if bmi >= 24 else "使用实际体重计算"
        })

    total_macros = calculate_macros(total_calories)

    return {
        "total_daily_calories": round(total_calories),
        "total_macros": total_macros,
        "per_meal_calories": {
            "breakfast": round(total_calories * 0.3),
            "lunch": round(total_calories * 0.4),
            "dinner": round(total_calories * 0.3),
        },
        "individual": individual,
    }


