def filter_recipes_by_restrictions(restrictions: list[str]) -> list:
    """根据忌口过滤菜谱"""
    available = []
    restriction_keywords = set()

    for r in restrictions:
        r_lower = r.lower()
        restriction_keywords.add(r_lower)
        # 扩展忌口匹配
        if "海鲜" in r or "鱼" in r or "虾" in r or "蟹" in r:
            restriction_keywords.update(["海鲜", "水产", "鱼", "虾", "蟹", "三文鱼", "带鱼", "鲈鱼", "鲫鱼", "虾仁", "鲜虾", "鳕鱼", "蛤蜊", "鱿鱼", "海参"])
        if "肉" in r or "荤" in r or "猪" in r or "牛" in r:
            restriction_keywords.update(["猪肉", "牛肉", "羊肉", "猪", "牛", "羊", "排骨", "牛腩", "里脊"])
        if "辣" in r or "辣椒" in r:
            restriction_keywords.update(["辣椒", "干辣椒", "辣椒油", "豆瓣酱", "花椒"])
        if "蛋" in r or "鸡蛋" in r:
            restriction_keywords.update(["鸡蛋", "蛋", "鹌鹑蛋"])
        if "奶" in r or "牛奶" in r or "乳" in r:
            restriction_keywords.update(["牛奶", "酸奶", "奶", "乳制品"])
        if "豆" in r or "豆腐" in r:
            restriction_keywords.update(["豆腐", "豆腐皮", "腐竹", "豆浆"])
        if "花生" in r or "坚果" in r:
            restriction_keywords.update(["花生", "核桃", "杏仁", "腰果"])

    for key, recipe in RECIPE_DB.items():
        skip = False
        # 检查食材
        for ing in recipe["ingredients"]:
            for rk in restriction_keywords:
                if rk in ing:
                    skip = True
                    break
            if skip:
                break
        # 检查标签
        if not skip:
            for tag in recipe.get("tags", []):
                for rk in restriction_keywords:
                    if rk in tag:
                        skip = True
                        break
            if skip:
                break
        if not skip:
            available.append(recipe)

    return available if available else list(RECIPE_DB.values())


def filter_by_conditions(recipes: list, conditions: str) -> list:
    """根据疾病状况调整菜品推荐"""
    if not conditions:
        return recipes

    cond_lower = conditions.lower()
    filtered = []

    for recipe in recipes:
        score = 1.0

        # 三高（高血压/高血脂/高血糖）偏好低脂低盐
        if any(kw in cond_lower for kw in ["三高", "高血压", "高血脂", "高血糖", "糖尿病"]):
            if recipe["cooking_method"] in ["蒸", "煮", "凉拌", "白灼"]:
                score += 0.3
            if recipe["cooking_method"] in ["炒", "炖"]:
                score += 0.1
            if "低脂" in recipe.get("tags", []):
                score += 0.2
            if "高蛋白" in recipe.get("tags", []):
                score += 0.1
            # 红烧、糖醋等减分
            if "红烧" in recipe["name"] or "糖醋" in recipe["name"]:
                score -= 0.3

        # 痛风避免高嘌呤（海鲜、内脏、红肉）
        if "痛风" in cond_lower:
            if recipe["category"] in ["海鲜", "荤菜"] and recipe.get("cuisine", "") not in ["粤菜"]:
                score -= 0.5
            if recipe["category"] in ["素菜", "豆制品", "蛋类", "主食"]:
                score += 0.2

        # 肠胃病偏好温和易消化
        if any(kw in cond_lower for kw in ["肠胃", "胃", "肠"]):
            if recipe["cooking_method"] in ["蒸", "煮", "炖"]:
                score += 0.3
            if "易消化" in recipe.get("tags", []):
                score += 0.3
            if "养胃" in recipe.get("tags", []):
                score += 0.3
            if "辣" in recipe.get("tags", []) or "开胃" in recipe.get("tags", []):
                score -= 0.2
            if recipe["name"] in ["手撕包菜", "麻婆豆腐"]:
                score -= 0.3

        # 返回评分>0的菜
        if score > 0:
            recipe["_health_score"] = score
            filtered.append(recipe)

    return filtered if filtered else recipes


def filter_by_preferences(recipes: list, pref: DietPreference) -> list:
    """根据饮食偏好筛选和排序"""
    scored = []
    for recipe in recipes:
        score = 1.0

        # 口味偏好匹配
        if "清淡" in pref.taste_pref:
            if recipe["cooking_method"] in ["蒸", "煮", "白灼", "凉拌"]:
                score += 0.3
        if "重口味" in pref.taste_pref or "辣" in pref.taste_pref:
            if recipe["cuisine"] in ["川菜", "湘菜"]:
                score += 0.2
            if "辣" in recipe.get("tags", []):
                score += 0.2
        if "酸甜" in pref.taste_pref:
            if "酸甜" in recipe.get("tags", []) or "番茄" in recipe["name"] or "糖醋" in recipe["name"]:
                score += 0.2

        # 菜系偏好
        if recipe["cuisine"] in pref.cuisine_pref:
            score += 0.3

        # 烹饪方式偏好
        if recipe["cooking_method"] in pref.cooking_method_pref:
            score += 0.2

        # 食物偏好匹配（食材级别）
        for like in pref.food_likes:
            for ing in recipe["ingredients"]:
                if like in ing or ing in like:
                    score += 0.15
                    break

        recipe["_pref_score"] = score
        scored.append(recipe)

    # 按评分排序
    scored.sort(key=lambda x: x.get("_pref_score", 1.0), reverse=True)
    return scored


def _scaled_recipe(recipe: dict, scale: float) -> dict:
    """按人数/碳水目标缩放一份菜谱，避免修改原菜谱库。"""
    item = copy.deepcopy(recipe)
    item["ingredients"] = {
        ing: max(1, int(round(qty * scale))) if isinstance(qty, (int, float)) else qty
        for ing, qty in item.get("ingredients", {}).items()
    }
    return item


def _dish_target(meal_count: int, difficulty: str, meal_type: str) -> int:
    """根据人数和烹饪难度计算菜品数；午晚餐米饭不计入，汤计入。"""
    offset = {"易": 0, "中": 1, "难": 2}.get(difficulty, 1)
    target = max(1, meal_count + offset)
    if meal_type in ["lunch", "dinner"]:
        return max(2, target)
    return target


def _rice_for_meal(meal_count: int, family_nutrition: dict, meal_ratio: float) -> dict:
    """按本餐碳水目标给午/晚餐配置熟米饭。"""
    rice = _scaled_recipe(RECIPE_DB["米饭"], 1)
    total_carb = family_nutrition.get("total_macros", {}).get("carb_g", 0)
    target_carb = total_carb * meal_ratio * 0.52
    rice_carb = target_carb * 0.78
    rice_grams = round(max(meal_count * 80, min(meal_count * 180, rice_carb / 25.9 * 100)))
    rice["name"] = "熟米饭"
    rice["ingredients"] = {"熟米饭": rice_grams}
    rice["health_benefit"] = f"约{rice_grams}g熟米饭，折合碳水约{round(rice_grams * 25.9 / 100)}g；其余碳水由早餐主食、蔬菜和水果分担。"
    rice["tags"] = ["碳水", "熟米饭", "午晚餐主食"]
    return rice


def _soup_for_slot(soup_dishes: list, slot_setting: MealSlotSetting, used: set) -> Optional[dict]:
    """按烹饪难度配汤；汤计入午晚餐菜品数。"""
    diff = slot_setting.cooking_difficulty
    all_soups = [r for r in RECIPE_DB.values() if r["category"] == "汤羹"]
    soup_names = {r["name"] for r in soup_dishes}
    soup_source = list(soup_dishes) + [r for r in all_soups if r["name"] not in soup_names]
    if diff in ["易", "中"]:
        pool = [
            r for r in soup_source
            if r["difficulty"] in ["易", "中"]
            and (r["cooking_method"] in ["煮"] or "清淡" in r.get("tags", []) or "低卡" in r.get("tags", []))
        ]
    else:
        pool = [
            r for r in soup_source
            if r["difficulty"] == "难"
            or r["cooking_method"] in ["炖"]
            or "肉汤" in r.get("tags", [])
            or "炖汤" in r.get("tags", [])
        ]
    pool = [r for r in pool if r["name"] not in used] or pool
    if not pool:
        return None
    item = copy.deepcopy(random.choice(pool))
    used.add(item["name"])
    return item


def _slot_members(members: list[FamilyMember], slot_setting: MealSlotSetting) -> list[FamilyMember]:
    """根据每餐勾选的成员计算实际用餐人，缺省时按人数取前几位。"""
    member_map = {m.id: m for m in members}
    selected = [member_map[mid] for mid in slot_setting.member_ids if mid in member_map]
    if selected:
        return selected
    count = max(1, min(slot_setting.meal_count, len(members)))
    return members[:count]


def _meal_nutrition(members: list[FamilyMember], slot_setting: MealSlotSetting) -> dict:
    selected = _slot_members(members, slot_setting)
    return calc_family_nutrition(selected)


def _meal_member_names(members: list[FamilyMember], slot_setting: MealSlotSetting) -> list[str]:
    selected = _slot_members(members, slot_setting)
    return [m.name or m.relationship or m.id for m in selected]


def match_recipes_to_meals(filtered_recipes: list, day_setting: DailyMealSetting, meal_nutrition: dict) -> dict:
    """为一日三餐匹配菜品（按主食/蛋白质/蔬菜/汤品规则搭配）。"""
    protein_dishes = [r for r in filtered_recipes if r["category"] in ["荤菜", "海鲜", "蛋类"]]
    veg_dishes = [r for r in filtered_recipes if r["category"] in ["素菜"]]
    soup_dishes = [r for r in filtered_recipes if r["category"] in ["汤羹"]]
    fruit_dishes = [r for r in filtered_recipes if r["category"] == "水果"]
    staple_dishes = [r for r in filtered_recipes if r["category"] in ["主食"]]
    breakfast_proteins = [
        r for r in filtered_recipes
        if r["category"] == "蛋类" or r["name"] in ["热豆浆", "豆腐脑"]
    ]
    breakfast_staples = [r for r in staple_dishes if r["name"] != "米饭"]
    if not protein_dishes:
        protein_dishes = [r for r in RECIPE_DB.values() if r["category"] in ["荤菜", "海鲜", "蛋类"]]
    if not veg_dishes:
        veg_dishes = [r for r in RECIPE_DB.values() if r["category"] == "素菜"]
    if not soup_dishes:
        soup_dishes = [r for r in RECIPE_DB.values() if r["category"] == "汤羹"]
    if not fruit_dishes:
        fruit_dishes = [r for r in RECIPE_DB.values() if r["category"] == "水果"]
    if not breakfast_staples:
        breakfast_staples = [r for r in RECIPE_DB.values() if r["category"] == "主食" and r["name"] != "米饭"]
    if not breakfast_proteins:
        breakfast_proteins = [r for r in RECIPE_DB.values() if r["category"] == "蛋类" or r["name"] in ["热豆浆", "豆腐脑"]]

    def _filter_by_slot(pool, slot_setting):
        diff = slot_setting.cooking_difficulty
        if diff == "易":
            f = [r for r in pool if r["difficulty"] in ["易"]]
            return f if f else pool
        elif diff == "中":
            f = [r for r in pool if r["difficulty"] in ["易", "中"]]
            return f if f else pool
        return pool

    def _veg_count(meal_count):
        return 1 if meal_count <= 2 else min(3, max(2, meal_count // 2))

    def _pick_main_and_veg(main_pool, veg_pool, dish_count, slot_setting, exclude):
        main_pool = _filter_by_slot(main_pool, slot_setting)
        veg_pool = _filter_by_slot(veg_pool, slot_setting)
        items = []
        if dish_count <= 0:
            return items
        if main_pool:
            picked = pick_dishes(main_pool, 1, exclude)
            items.extend(picked)
            exclude.update(r["name"] for r in picked)
        remaining = dish_count - len(items)
        if remaining > 0 and veg_pool:
            picked = pick_dishes(veg_pool, remaining, exclude)
            items.extend(picked)
            exclude.update(r["name"] for r in picked)
        remaining = dish_count - len(items)
        if remaining > 0:
            mixed_pool = main_pool + veg_pool
            picked = pick_dishes(mixed_pool, remaining, exclude)
            items.extend(picked)
            exclude.update(r["name"] for r in picked)
        return items[:dish_count]

    def pick_dishes(pool, count, exclude=None):
        exclude = exclude or set()
        p = [r for r in pool if r["name"] not in exclude]
        if len(p) < count:
            p = list(pool)
        random.shuffle(p)
        return p[:count]

    used = set()
    result = {}

    # === 早餐 ===
    bf_s = day_setting.breakfast
    bf_count = max(1, len(meal_nutrition["breakfast"].get("individual", [])))
    bf_staple = _filter_by_slot(breakfast_staples, bf_s)
    bf_protein = _filter_by_slot(breakfast_proteins, bf_s)
    bf_target = _dish_target(bf_count, bf_s.cooking_difficulty, "breakfast")
    bf_items = []
    if bf_s.cooking_difficulty == "难" and fruit_dishes and bf_target > 0:
        fruit = pick_dishes(fruit_dishes, 1, used)
        bf_items.extend(_scaled_recipe(r, max(0.7, bf_count / 2)) for r in fruit)
        used.update(r["name"] for r in fruit)
    if bf_staple:
        items = pick_dishes(bf_staple, 1, used)
        bf_items.extend(_scaled_recipe(r, max(0.8, bf_count / 2)) for r in items)
        used.update(r["name"] for r in items)
    if bf_protein:
        items = pick_dishes(bf_protein, 1, used)
        bf_items.extend(_scaled_recipe(r, max(0.8, bf_count / 2)) for r in items)
        used.update(r["name"] for r in items)
    while len(bf_items) < bf_target:
        extra_source = bf_staple + bf_protein + (fruit_dishes if bf_s.cooking_difficulty == "难" else [])
        extra_pool = [r for r in extra_source if r["name"] not in used]
        if not extra_pool:
            break
        item = random.choice(extra_pool)
        bf_items.append(_scaled_recipe(item, max(0.7, bf_count / 2)))
        used.add(item["name"])
    bf_items = bf_items[:bf_target]
    result["breakfast"] = bf_items

    # === 午餐 ===
    ln_s = day_setting.lunch
    ln_count = max(1, len(meal_nutrition["lunch"].get("individual", [])))
    ln_target = _dish_target(ln_count, ln_s.cooking_difficulty, "lunch")
    ln_soup = _soup_for_slot(soup_dishes, ln_s, used)
    ln_non_soup_count = ln_target - (1 if ln_soup else 0)
    ln_items = [_rice_for_meal(ln_count, meal_nutrition["lunch"], 0.4)]
    ln_items.extend(
        _scaled_recipe(r, max(1, ln_count / 2))
        for r in _pick_main_and_veg(protein_dishes, veg_dishes, ln_non_soup_count, ln_s, used)
    )
    result["lunch"] = ln_items
    result["lunch_soup"] = ln_soup

    # === 晚餐 ===
    dn_s = day_setting.dinner
    dn_count = max(1, len(meal_nutrition["dinner"].get("individual", [])))
    dn_target = _dish_target(dn_count, dn_s.cooking_difficulty, "dinner")
    dn_soup = _soup_for_slot(soup_dishes, dn_s, used)
    dn_non_soup_count = dn_target - (1 if dn_soup else 0)
    dn_items = [_rice_for_meal(dn_count, meal_nutrition["dinner"], 0.3)]
    dn_items.extend(
        _scaled_recipe(r, max(1, dn_count / 2))
        for r in _pick_main_and_veg(protein_dishes, veg_dishes, dn_non_soup_count, dn_s, used)
    )
    result["dinner"] = dn_items
    result["dinner_soup"] = dn_soup
    all_dinner_fruits = [
        r for r in RECIPE_DB.values()
        if r["category"] == "水果" and "酸奶" not in r.get("ingredients", {}) and "牛奶" not in r.get("ingredients", {})
    ]
    dinner_fruit_pool = [r for r in fruit_dishes if r["name"] in {f["name"] for f in all_dinner_fruits}]
    dinner_fruit_pool = dinner_fruit_pool or [r for r in all_dinner_fruits if r["name"] not in used] or all_dinner_fruits
    fruit = pick_dishes(dinner_fruit_pool, 1, used)
    result["dinner_fruit"] = _scaled_recipe(fruit[0], max(0.7, dn_count / 2)) if fruit else None

    return result


def generate_weekly_plan(request: MealPlanRequest) -> dict:
    """生成一周食谱"""
    # 计算家庭营养需求
    nutrition = calc_family_nutrition(request.members)
    fridge_recipes = _match_fridge_ingredients(request.fridge_ingredients) if request.fridge_ingredients else []

    # 过滤可用菜谱
    available = filter_recipes_by_restrictions(request.preferences.food_restrictions)
    available = filter_by_conditions(available, " ".join([m.conditions for m in request.members]))
    available = filter_by_preferences(available, request.preferences)

    weekly_plan = {
        "nutrition_summary": nutrition,
        "days": {},
    }

    used_recipes_week = set()  # 追踪本周已用菜名

    for setting in request.daily_settings:
        day_name = setting.day
        # 每日使用时重新洗牌菜谱以获得变化
        day_recipes = [r for r in available if r["name"] not in used_recipes_week]
        if len(day_recipes) < 10:
            day_recipes = list(available)

        meal_nutrition = {
            "breakfast": _meal_nutrition(request.members, setting.breakfast),
            "lunch": _meal_nutrition(request.members, setting.lunch),
            "dinner": _meal_nutrition(request.members, setting.dinner),
        }
        meals = match_recipes_to_meals(day_recipes, setting, meal_nutrition)

        # 追踪本周使用的菜
        for meal_list in [meals["breakfast"], meals["lunch"], meals["dinner"]]:
            for m in meal_list:
                used_recipes_week.add(m["name"])
        if meals.get("soup"):
            used_recipes_week.add(meals["soup"]["name"])
        if meals.get("lunch_soup"):
            used_recipes_week.add(meals["lunch_soup"]["name"])
        if meals.get("dinner_soup"):
            used_recipes_week.add(meals["dinner_soup"]["name"])
        if meals.get("dinner_fruit"):
            used_recipes_week.add(meals["dinner_fruit"]["name"])

        weekly_plan["days"][day_name] = {
            "setting": {
                "breakfast": {
                    "meal_count": len(meal_nutrition["breakfast"]["individual"]),
                    "cooking_difficulty": setting.breakfast.cooking_difficulty,
                    "member_ids": [m["id"] for m in meal_nutrition["breakfast"]["individual"]],
                    "member_names": _meal_member_names(request.members, setting.breakfast),
                },
                "lunch": {
                    "meal_count": len(meal_nutrition["lunch"]["individual"]),
                    "cooking_difficulty": setting.lunch.cooking_difficulty,
                    "member_ids": [m["id"] for m in meal_nutrition["lunch"]["individual"]],
                    "member_names": _meal_member_names(request.members, setting.lunch),
                },
                "dinner": {
                    "meal_count": len(meal_nutrition["dinner"]["individual"]),
                    "cooking_difficulty": setting.dinner.cooking_difficulty,
                    "member_ids": [m["id"] for m in meal_nutrition["dinner"]["individual"]],
                    "member_names": _meal_member_names(request.members, setting.dinner),
                },
            },
            "meals": meals,
        }

    # 确保偏好食材每周出现2次以上
    _ensure_preference_frequency(weekly_plan, request.preferences, available)
    _apply_fridge_to_early_days(weekly_plan, fridge_recipes)

    weekly_plan["fridge_suggestions"] = fridge_recipes
    weekly_plan["weekly_quote"] = WEEKLY_QUOTES[0]
    weekly_plan["generated_at"] = datetime.now().isoformat()

    return weekly_plan


def _ensure_preference_frequency(plan: dict, pref: DietPreference, available: list):
    """确保偏好食物每周至少出现2次"""
    for like in pref.food_likes:
        count = 0
        for day_name, day_data in plan.get("days", {}).items():
            meals = day_data.get("meals", {})
            for meal_type in ["breakfast", "lunch", "dinner"]:
                for recipe in meals.get(meal_type, []):
                    for ing in recipe.get("ingredients", {}):
                        if like in ing or ing in like:
                            count += 1
                            break

        # 如果偏好食物出现不足2次，尝试替换
        if count < 2 and like:
            # 找含有该偏好的菜
            preferred = []
            for r in available:
                for ing in r.get("ingredients", {}):
                    if like in ing or ing in like:
                        preferred.append(r)
                        break

            if preferred:
                # 找一个空闲位置替换
                days = list(plan.get("days", {}).keys())
                for _ in range(2 - count):
                    day = random.choice(days)
                    meal_type = "lunch" if random.random() > 0.5 else "dinner"
                    if plan["days"][day]["meals"].get(meal_type):
                        new_recipe = random.choice(preferred)
                        meal = plan["days"][day]["meals"][meal_type]
                        replace_idx = None
                        if new_recipe["category"] in ["荤菜", "海鲜", "蛋类"]:
                            for idx, recipe in enumerate(meal):
                                if recipe.get("category") in ["荤菜", "海鲜", "蛋类"]:
                                    replace_idx = idx
                                    break
                        elif new_recipe["category"] == "素菜":
                            for idx, recipe in enumerate(meal):
                                if recipe.get("category") == "素菜":
                                    replace_idx = idx
                                    break
                        if replace_idx is not None and meal[replace_idx].get("category") != "主食":
                            meal[replace_idx] = new_recipe


def _apply_fridge_to_early_days(plan: dict, fridge_recipes: list):
    """冰箱余材优先安排在周一/周二，避免拖到后半周才吃。"""
    if not fridge_recipes:
        return

    used = set()
    for day in ["周一", "周二"]:
        day_data = plan.get("days", {}).get(day)
        if not day_data:
            continue
        meals = day_data.get("meals", {})

        for fridge_recipe in fridge_recipes:
            name = fridge_recipe.get("name")
            category = fridge_recipe.get("category")
            if not name or name in used or category == "主食":
                continue

            candidate = copy.deepcopy(fridge_recipe)
            candidate.setdefault("tags", [])
            if "余材优先" not in candidate["tags"]:
                candidate["tags"].append("余材优先")
            candidate["_fridge_used"] = True

            placed = False
            if category == "汤羹":
                day_soup_names = {
                    meals.get("lunch_soup", {}).get("name"),
                    meals.get("dinner_soup", {}).get("name"),
                }
                for soup_key in ["lunch_soup", "dinner_soup"]:
                    other_key = "dinner_soup" if soup_key == "lunch_soup" else "lunch_soup"
                    other_name = meals.get(other_key, {}).get("name")
                    if meals.get(soup_key) and not meals[soup_key].get("_fridge_used") and name != other_name:
                        meals[soup_key] = candidate
                        placed = True
                        break
                if placed:
                    day_soup_names.add(name)
            else:
                target_categories = (
                    ["荤菜", "海鲜", "蛋类", "豆制品"] if category in ["荤菜", "海鲜", "蛋类", "豆制品"]
                    else ["素菜"] if category == "素菜"
                    else [category]
                )
                for meal_key in ["lunch", "dinner"]:
                    meal_list = meals.get(meal_key, [])
                    for idx, recipe in enumerate(meal_list):
                        if (
                            recipe.get("category") in target_categories
                            and recipe.get("category") != "主食"
                            and not recipe.get("_fridge_used")
                        ):
                            meal_list[idx] = candidate
                            placed = True
                            break
                    if placed:
                        break

            if placed:
                used.add(name)
                if len(used) >= 4:
                    return


def _match_fridge_ingredients(ingredients_str: str) -> list:
    """根据冰箱食材匹配菜品"""
    ingredients = [i.strip() for i in ingredients_str.replace("、", ",").replace("，", ",").split(",") if i.strip()]
    if not ingredients:
        return []

    matched = []
    for key, recipe in RECIPE_DB.items():
        score = 0
        total_ings = len(recipe["ingredients"])
        matched_ings = []

        for ing in recipe["ingredients"]:
            for fridge_ing in ingredients:
                if fridge_ing in ing or ing in fridge_ing:
                    score += 1
                    matched_ings.append(ing)
                    break

        if score > 0:
            recipe_copy = dict(recipe)
            recipe_copy["_match_score"] = score
            recipe_copy["_match_ratio"] = round(score / total_ings * 100)
            recipe_copy["_matched_ingredients"] = matched_ings
            matched.append(recipe_copy)

    matched.sort(key=lambda x: x.get("_match_score", 0), reverse=True)
    return matched[:8]


def swap_recipe(recipe_name: str) -> dict:
    """替换菜品"""
    similar = SIMILAR_RECIPES.get(recipe_name, [])
    available_similar = []
    for name in similar:
        if name in RECIPE_DB:
            available_similar.append(RECIPE_DB[name])

    # 如果同类不够，找同分类的
    if len(available_similar) < 3:
        orig = RECIPE_DB.get(recipe_name)
        if orig:
            category = orig["category"]
            for key, recipe in RECIPE_DB.items():
                if recipe["category"] == category and recipe["name"] != recipe_name and recipe["name"] not in similar:
                    similar.append(key)
                    available_similar.append(recipe)

    return {
        "original": recipe_name,
        "alternatives": available_similar[:5],
    }


