# ==================== 数据模型 ====================

class FamilyMember(BaseModel):
    id: str
    name: str
    gender: str  # male / female
    height: float  # cm
    weight: float  # kg
    relationship: str  # 本人/妻子/丈夫/儿子/女儿/父亲/母亲/岳父/岳母/公公/婆婆/舍友/其他
    allergies: str = ""  # 过敏史
    conditions: str = ""  # 基础疾病

class DietPreference(BaseModel):
    taste_pref: list[str] = []  # 清淡/重口味/酸甜/辣/咸/苦
    food_likes: list[str] = []  # 偏好的食物类别
    food_restrictions: list[str] = []  # 忌口食物
    cuisine_pref: list[str] = []  # 菜系偏好：川菜/粤菜/鲁菜/苏菜/家常/西式/日式等
    cooking_method_pref: list[str] = []  # 烹饪方式偏好：炒/蒸/炖/煮/烤/凉拌

class MealSlotSetting(BaseModel):
    meal_count: int = 3  # 该餐用餐人数
    cooking_difficulty: str = "中"  # 难/中/易
    member_ids: list[str] = []  # 该餐实际用餐成员ID

class DailyMealSetting(BaseModel):
    day: str  # 周一~周日
    breakfast: MealSlotSetting = MealSlotSetting()
    lunch: MealSlotSetting = MealSlotSetting()
    dinner: MealSlotSetting = MealSlotSetting()

class MealPlanRequest(BaseModel):
    members: list[FamilyMember]
    preferences: DietPreference
    daily_settings: list[DailyMealSetting]
    fridge_ingredients: str = ""  # 冰箱余材

